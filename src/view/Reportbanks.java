    void loadTable2() {
        clearTable();
        DefaultTableModel dt = (DefaultTableModel) tblsites.getModel();
        int temprec = 0;
        double temptotal = 0.0;
        
        try {
            ResultSet rs = site.getBankPayments(ycyear.getYear(), mcmonth.getMonth()+1, 1);
            ResultSet rs2 = site.getBankPayments2(ycyear.getYear(), mcmonth.getMonth()+1, 1);
            rs.next();
            rs2.next();
            
            comrecords = rs.getInt("numrec");
            comtotal = rs.getDouble("tot");
            
            otherrecords = rs2.getInt("numrec");
            othertotal = rs2.getDouble("tot");
            
            allrecords = comrecords + otherrecords;
            finaltotal = comtotal + othertotal;
            
            Vector vec = new Vector();
            Vector vec2 = new Vector();
            Vector vec3 = new Vector();
            
            vec.add("Commercial");
            vec.add(comrecords);
            vec.add(comtotal);
            dt.addRow(vec);
            
            vec2.add("Other");
            vec2.add(otherrecords);
            vec2.add(othertotal);
            dt.addRow(vec2);
            
            vec3.add("All");
            vec3.add(allrecords);
            vec3.add(finaltotal);
            dt.addRow(vec3);
            
            System.out.println("COM Recs: " + comrecords);
            System.out.println("COM TOTAL: " + comtotal);
            
            System.out.println("Other Recs: " + otherrecords);
            System.out.println("Other TOTAL: " + othertotal);
            
            System.out.println("All Recs: " + allrecords);
            System.out.println("All TOTAL: " + finaltotal);
            


        } catch (Exception e) {
            System.out.println("Error: " + e);
            e.printStackTrace();
        }

    }

    void clearTable() {
        DefaultTableModel dt = (DefaultTableModel) tblsites.getModel();
        int rows = tblsites.getRowCount();
        for (int x = 0; x < rows; x++) {
            dt.removeRow(0);
        }

    }

