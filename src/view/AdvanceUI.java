    public void updateTable(){
    DefaultTableModel df = (DefaultTableModel) tblsal.getModel();
        df.setRowCount(0);
        try {
            ResultSet rs = new Employee().getEmpAdvance(Employee.ALLEMPLOYES, ycyear.getYear(), mcmonth.getMonth() + 1);
            while (rs.next()) {
                ArrayList ar = new ArrayList();
                ar.add(rs.getString("EmpNumber"));
                ar.add(rs.getString("FMName") + " " + rs.getString("LName"));
                ar.add(rs.getString("Advance"));
                ar.add(rs.getString("Date")+" "+rs.getString("Time"));
                ar.add(rs.getString("year"));
                ar.add(rs.getString("month"));
                df.addRow(ar.toArray());
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

