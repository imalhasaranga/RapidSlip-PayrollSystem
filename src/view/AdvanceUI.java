    private void tblsalMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblsalMouseClicked
        if (tblsal.getSelectedRow() != -1) {
            bsave.setEnabled(false);
            int empno1 = Integer.parseInt(tblsal.getValueAt(tblsal.getSelectedRow(), 0).toString());
            String year = tblsal.getValueAt(tblsal.getSelectedRow(), 4).toString();
            String month = tblsal.getValueAt(tblsal.getSelectedRow(), 5).toString();
            emp = new Employee();
            
            emp.setEmpno(empno1);
            emp.setAdvanceTaknYear(Integer.parseInt(year));
            emp.setAvannceTakinMonth(Integer.parseInt(month));
            ResultSet rs = emp.getEmpAdvance(Employee.THISEMPLOYE, ycyear.getYear(), mcmonth.getMonth() + 1);
            try {
                if (rs.next()) {
                    tename.setText(rs.getString("FMName") + " " + rs.getString("LName") + "-" + rs.getString("EmpNumber"));
                    tadvance.setText(rs.getString("Advance"));
                    ycyear.setYear(rs.getInt("year"));
                    mcmonth.setMonth(rs.getInt("month") - 1);
                    lenum.setText("Emp.No: " + empno1);
                    lepfnum.setText("EPF. No: " + rs.getString("EpFNumber"));

                }

            } catch (Exception e) {
                System.out.println("error in table click" + e);
            }
        }
    }//GEN-LAST:event_tblsalMouseClicked

