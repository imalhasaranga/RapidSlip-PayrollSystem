            this.basicsalary = Double.parseDouble(tblsal.getValueAt(tblsal.getSelectedRow(), 2).toString());
            this.gvtallowance = Double.parseDouble(tblsal.getValueAt(tblsal.getSelectedRow(), 3).toString());
            this.budallowance = Double.parseDouble(tblsal.getValueAt(tblsal.getSelectedRow(), 4).toString());
            
