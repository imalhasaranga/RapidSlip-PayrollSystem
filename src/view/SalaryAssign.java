    boolean formCheck() {
        boolean val = false;

        try {
            if (tename.getText().trim().equals("") || empno == 0 || tbasicsal.getText().trim().equals("") || tgvtallowance.getText().trim().equals("") || tbudallowance.getText().trim().equals("")) {
                val = false;
            } else {
                val = true;
            }
        } catch (Exception e) {
            val = false;
        }

        return val;
    }

    void loadTable(ResultSet rs) {
        clearTable();
        DefaultTableModel dt = (DefaultTableModel) tblsal.getModel();

        try {
            while (rs.next()) {
                ArrayList vec = new ArrayList<>();
                vec.add(rs.getInt("EmpNumber"));
                vec.add(rs.getString("FMname") + " " + rs.getString("Lname"));
                vec.add(rs.getDouble("Basic_salary"));
                vec.add(rs.getDouble("gvtAllow"));
                vec.add(rs.getDouble("budAllow"));
                dt.addRow(vec.toArray());
            }

        } catch (Exception e) {
            System.out.println("Error From loadTable Method: " + e);
        }

    }

    void clearTable() {
        DefaultTableModel dt = (DefaultTableModel) tblsal.getModel();
        int rows = tblsal.getRowCount();
        for (int x = 0; x < rows; x++) {
            dt.removeRow(0);
        }

    }

