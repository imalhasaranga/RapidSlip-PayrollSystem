    private void baddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_baddActionPerformed
        if(!tsite.getText().trim().equals("")){
            Site site = new Site();
            site.setSite(tsite.getText());
                if(site.addSite()){
                    JOptionPane.showMessageDialog(null, "Done! Added Successfully!", "Done!", 1);
                    loadTable(Site.getSites());
                    clearAll();
                }else{
                    JOptionPane.showMessageDialog(null, "Error While Adding Site!", "Error!", 0);
                }
            
        }else{
            JOptionPane.showMessageDialog(null, "Type the Site to Add!", "Warning!", 2);
        }
    }//GEN-LAST:event_baddActionPerformed

    private void tblsiteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblsiteMouseClicked
        badd.setEnabled(false);
        bedit.setEnabled(true);
        bdeactivate.setEnabled(true);
        
        try {
            updateid = Integer.parseInt(tblsite.getValueAt(tblsite.getSelectedRow(), 0).toString());
            tsite.setText(tblsite.getValueAt(tblsite.getSelectedRow(), 1).toString());
        } catch (Exception e) {
            System.out.println("No Record Selected! " + e);
        }
        
        
    }//GEN-LAST:event_tblsiteMouseClicked

    private void beditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_beditActionPerformed
        if(updateid != 0){
            if(!tsite.getText().trim().equals("")){
                Site site = new Site();
                site.setSite(tsite.getText());
                    if(site.updateSite(updateid)){
                        JOptionPane.showMessageDialog(null, "Done! Updated Successfully!", "Done!", 1);
                        loadTable(Site.getSites());
                        clearAll();
                    }else{
                        JOptionPane.showMessageDialog(null, "Error While Updating Site!", "Error!", 0);
                    }
            
            }else{
                JOptionPane.showMessageDialog(null, "Type the Site to Update!", "Warning!", 2);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Select an Site From Table!", "Warning!", 2);
        }
    }//GEN-LAST:event_beditActionPerformed

    private void bdeactivateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdeactivateActionPerformed
        if(updateid != 0){
            if(JOptionPane.showConfirmDialog(null, "Sure to Deactivate Site No " + updateid + "?", "Warning!", 0, 1) == 0){
                Site site = new Site();
                    if(site.deactivateSite(updateid)){
                        loadTable(Site.getSites());
                        JOptionPane.showMessageDialog(null, "Done! Deactivated Site No: " + updateid, "Done!", 1);
                    }else{
                        JOptionPane.showMessageDialog(null, "Error While Deactivating Site!", "Error!", 0);
                    }
            }
        }else{
            JOptionPane.showMessageDialog(null, "Select Site to Deactivate!", "Warning!", 2);
        }
    }//GEN-LAST:event_bdeactivateActionPerformed
    
    void loadTable(ResultSet rs){
        clearTable();
        DefaultTableModel dt = (DefaultTableModel) tblsite.getModel();
        
        try {
            while(rs.next()){
                ArrayList vec = new ArrayList();
                vec.add(rs.getInt("site_ID"));
                vec.add(rs.getString("Site_location"));
                
                dt.addRow(vec.toArray()); 
            }
                    
        } catch (Exception e) {
            System.out.println("Error From loadTable Method: " + e);
        }
        
    }
    
    void clearTable(){
        DefaultTableModel dt = (DefaultTableModel) tblsite.getModel();
        int rows = tblsite.getRowCount();
            for(int x=0; x < rows; x++){
                dt.removeRow(0); 
            }
        
    }
    
    void clearAll(){
        tsite.setText("");
    }
