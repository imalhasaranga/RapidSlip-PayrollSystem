    private void barrangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_barrangeActionPerformed

        DefaultTableModel dt = (DefaultTableModel) tblemployees.getModel();

        try {
            ResultSet rs = DB.getData("SELECT EmpNumber, FMname FROM employee");
            while (rs.next()) {
                String var_name = "";
                Vector vec = new Vector();
                empnumber = rs.getInt("EmpNumber");
                empname = rs.getString("FMname");
                name = empname.split(" ");
                lname = name[name.length - 1];

                for (int x = 0; x < (name.length - 1); x++) {
                    var_name += name[x] + " ";
                }
                fmname = var_name.substring(0, (var_name.length() - 1));

                vec.add(empnumber);
                vec.add(empname);
                vec.add(fmname);
                vec.add(lname);
                dt.addRow(vec);

            }
        } catch (Exception e) {
            System.out.println("Error While Arranging!" + e);
            e.printStackTrace();
        }

        System.out.println("Rows: " + tblemployees.getRowCount());
    }//GEN-LAST:event_barrangeActionPerformed

    private void barrange1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_barrange1ActionPerformed
        if (JOptionPane.showConfirmDialog(null, "Change Data Base?", "Warning!", 0, 2) == 0) {
            for (int x = 0; x < tblemployees.getRowCount(); x++) {
                updateid = Integer.parseInt(tblemployees.getValueAt(x, 0).toString());
                up_fname = tblemployees.getValueAt(x, 2).toString();
                up_lname = tblemployees.getValueAt(x, 3).toString();
                
                try {
                    DB.setData("update employee set FMname = '" + up_fname + "', Lname = '" + up_lname + "' where EmpNumber = '" + updateid + "'");
                    lmsg.setText("Update Success For EmpNo: " + updateid);
                } catch (Exception e) {
                    System.out.println("Error While Changing Data Base!" + e);
                    e.printStackTrace();
                }
            }
        }


    }//GEN-LAST:event_barrange1ActionPerformed

