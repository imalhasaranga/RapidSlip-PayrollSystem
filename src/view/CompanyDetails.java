    public void getCompany() {


        try {
            ResultSet rs = DB.getData("SELECT 	"
                    + "Compname, "
                    + "Address, "
                    + "contact, "
                    + "contact2, "
                    + "logourl, "
                    + "web, "
                    + "email,"
                    + "fax, "
                    + "comp_bankName, "
                    + "accountnumber, "
                    + "MCIRNO, "
                    + "BranchNo"
                    + " FROM "
                    + "comp_parameters ");
            if (rs.next()) {

                companynae.setText(rs.getString("Compname"));
                address.setText(rs.getString("Address"));
                nub1.setText(rs.getString("contact"));
                nub2.setText(rs.getString("contact2"));
                logo.setText(rs.getString("logourl"));
                web.setText(rs.getString("web"));
                email.setText(rs.getString("email"));
                fax.setText(rs.getString("fax"));
                banckComapny.setText(rs.getString("comp_bankName"));
                accountnamuber.setText(rs.getString("accountnumber"));
                mcirno.setText(rs.getString("MCIRNO"));
                branchno.setText(rs.getString("BranchNo"));
            }

        } catch (Exception e) {
            System.out.println(e);
        }



    }

