    private void bview1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bview1ActionPerformed
        if (JOptionPane.showConfirmDialog(null, "Want to Print the Table!", "Question", 0, 3) == 0) {
            sitetotal = new PrintSiteTotal();
            sitetotal.setYear(ycyear.getYear());
            sitetotal.setMonth(mcmonth.getMonth() + 1);
            
            Vector vecsite = new Vector();
            Vector veccashamount = new Vector();
            Vector vecbankamount = new Vector();
            Vector vecamount = new Vector();
            
            for (int x = 0; x < tblsites.getRowCount(); x++) {
                vecsite.add(tblsites.getValueAt(x, 0));
                veccashamount.add(tblsites.getValueAt(x, 1));
                vecbankamount.add(tblsites.getValueAt(x, 2));
                vecamount.add(tblsites.getValueAt(x, 3));
            }
            
            sitetotal.setSitename(vecsite);
            sitetotal.setCashamount(veccashamount);
            sitetotal.setBankamount(vecbankamount);
            sitetotal.setSiteamount(vecamount);
            
            sitetotal.setCashtotalamount(totalcashamount);
            sitetotal.setBanktotalamount(totalbankamount);
            sitetotal.setTotalamount(totalcashamount+totalbankamount);
            
            sitetotal.printThisInvoice();
        }




    }//GEN-LAST:event_bview1ActionPerformed

    void loadTable() {
        clearTable();
        DefaultTableModel dt = (DefaultTableModel) tblsites.getModel();

        try {

            ResultSet rs2 = DB.getData("select * from site where is_active = '1'");


            while (rs2.next()) {
                ResultSet rs3 = Site.getSitePayAmount(ycyear.getYear(), mcmonth.getMonth() + 1, rs2.getInt("site_ID"));
                rs3.next();
                ArrayList vec = new ArrayList();

                vec.add(rs2.getString("site_location"));
                vec.add(rs3.getDouble("finaltotal"));
                dt.addRow(vec.toArray());

                totalsalamount += rs3.getDouble("finaltotal");

            }

        } catch (Exception e) {
            System.out.println("Error From loadTable Method: " + e);
        }
        
    }
    
    void loadTable2() {
        clearTable();
        DefaultTableModel dt = (DefaultTableModel) tblsites.getModel();
        totalcashamount = 0.0;
        totalbankamount = 0.0;
        
        try {

            ResultSet rs2 = DB.getData("select * from site where is_active = '1'");


            while (rs2.next()) {
                double banktotal = 0.0;
                double cashtotal = 0.0;
                
                
                
                ResultSet rs3 = Site.getSitePayAmount2(ycyear.getYear(), mcmonth.getMonth() + 1, rs2.getInt("site_ID"));
                
                    while(rs3.next()){
                        
                        if(rs3.getString("PayType").equals("CASH")){
                            cashtotal += rs3.getDouble("PackSal");
                            System.out.println("VAL1: " + rs3.getDouble("PackSal"));
                        }else if(rs3.getString("PayType").equals("BANK")){
                            banktotal += rs3.getDouble("PackSal");
                            System.out.println("VAL3: " + rs3.getDouble("PackSal"));
                        }
                        
                    }
                    
                    totalcashamount += cashtotal;
                    totalbankamount += banktotal;
                    
                    
                ArrayList vec = new ArrayList();

                vec.add(rs2.getString("site_location"));
                vec.add(cashtotal);
                vec.add(banktotal);
                vec.add(cashtotal+banktotal);
                dt.addRow(vec.toArray());

                //totalsalamount += rs3.getDouble("finaltotal");

            }
            totalsalamount = totalcashamount + totalbankamount;
            showValues();
            
        } catch (Exception e) {
            System.out.println("Error From loadTable Method: " + e);
            e.printStackTrace();
        }
        
    }

    void clearTable() {
        DefaultTableModel dt = (DefaultTableModel) tblsites.getModel();
        int rows = tblsites.getRowCount();
        for (int x = 0; x < rows; x++) {
            dt.removeRow(0);
        }

    }
    
    void showValues(){
        ltotcash.setText(""+formatForDouble(totalcashamount));
        ltotbank.setText(""+formatForDouble(totalbankamount));
        ltotsalary.setText(""+formatForDouble(totalsalamount));
        
    }
    
    public String formatForDouble(double contents) {
        String newFormNo = "";
        try {
            NumberFormat nf = NumberFormat.getNumberInstance();
            nf.setMaximumFractionDigits(2);
            nf.setMinimumFractionDigits(2);
            newFormNo = nf.format(contents);
        } catch (Exception ex) {
        }
        return newFormNo;
    }

