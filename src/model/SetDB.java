private void bsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsaveActionPerformed
    if(thost.getText().trim().equals("") || tport.getText().trim().equals("") || tusername.getText().trim().equals("") || tpassword.getText().trim().equals("")){
        JOptionPane.showMessageDialog(null, "Fill all textboxes!", "Warning!", 0);
    }else{
        int confirmval = JOptionPane.showConfirmDialog(null, "Are you sure to Save/Change the Data Base Configuration Details?", "Question!", 0); 
        
        if(confirmval == 0){
            try {
                FileOutputStream dbdata = new FileOutputStream(System.getProperty("user.dir") + "/dbdata.dat");
                ObjectOutputStream out = new ObjectOutputStream(dbdata);
                DbProperty dbproperty = new DbProperty();
                
                dbproperty.setHost(thost.getText()); 
                dbproperty.setPort(tport.getText());
                dbproperty.setUsername(tusername.getText());
                dbproperty.setPassword(tpassword.getText());
                
                out.writeObject(dbproperty); 
                out.close();
                
                Class.forName(dbproperty.getDriver()).newInstance();
                DriverManager.getConnection("jdbc:mysql://" + dbproperty.getHost() + ":" + dbproperty.getPort() + "/payrolldata?autoReconnect=true", dbproperty.getUsername(), dbproperty.getPassword());
                
                this.dispose();
                
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: Connection Failure, Please check the configuration details!", "Connection Failure", 0); 
                System.out.println("Error:" + e);
            }
            
            
        }
    }
    
    
        
}//GEN-LAST:event_bsaveActionPerformed

